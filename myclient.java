import java.net.*;
import java.io.*;
public class myclient
{
public static void main(String args[])
{
try
{
Socket client=new Socket("157.0.0.1",1234);
Socket client1=new Socket(InetAddress.getLocalHost(),1234);
InputStream clientin=client.getInputStream();
OutputStream clientout=client.getOutputStream();
PrintWriter pw=new PrintWriter(clientout,true);
BufferedReader br=new BufferedReader(new InputStreamReader(clientin));
BufferedReader stdin=new BufferedReader(new InputStreamReader(System.in));
System.out.print("type a message for server");
pw.println(stdin.readLine());
System.out.print("Server message:");
System.out.print(br.readLine());
pw.close();
br.close();
client.close();
}
catch(ConnectException ce)
{
System.out.print("cannot connect to the server");
}
catch(IOException ie)
{
System.out.println("IO Error");
}
}
}